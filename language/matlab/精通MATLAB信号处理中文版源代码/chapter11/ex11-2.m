clc;close all;clear all;
T=0.48;ts=0.001;fc=400;
A=4;fs=1/ts;
t=[0:ts:T];
m=[2*ones(1,T/(4*ts)),-1*ones(1,T/(2*ts)),zeros(1,T/(4*ts)+1)];
c=cos(2*pi*fc.*t);
am=(A*(1+m)).*c;
dsb=m.*c;
f=(1:1024).*fs/1024;
m_spec=abs(fft(m,1024));
dsb_spec=abs(fft(dsb,1024));
am_spec=abs(fft(am,1024));
subplot(321);
plot(m);
title('基带信号时域波形');
xlabel('t');
ylabel('幅度');
subplot(323);
plot(dsb);
title('DSB-AM调制信号时域波形');
xlabel('t');
ylabel('幅度');
subplot(325);
plot(am);
title('AM调制信号时域波形');
xlabel('t');
ylabel('幅度');
subplot(322);
plot(f,m_spec);
title('基带信号频谱');
xlabel('f/Hz');
ylabel('M(f)');
subplot(324);
plot(f,dsb_spec);
title('DSB-AM调制信号频谱');
xlabel('f/Hz');
ylabel('DSB AM(f)');
subplot(326);
plot(f,am_spec);
title('AM调制信号频谱');
xlabel('f/Hz');y
label('AM(f)');
