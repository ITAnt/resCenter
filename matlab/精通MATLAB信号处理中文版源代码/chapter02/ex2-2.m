clear all;
dt=0.01;n=0:90-1;
t=n*dt;
f=10;       %原始信号的频率为10Hz
x=sin(3*pi*f*t+0.5);  %在计算机上的原始信号
dt=0.1;
n=0:10-1;
t1=n*dt;
% 以10Hz的采样频率采样,为取一样的时间长度
% 序号长度为原始信号序号长度的1/10
x1=sin(3*pi*f*t1+0.5);  %采样后的信号
subplot(3,1,1);plot(t,x);
% 绘出模拟原始信号,为与下图统一,采样y轴的范围[-1 1]用ylim给出
ylim([-1,1]);
title('原始信号');
% 绘出在模拟信号基础上的采样过程
subplot(3,1,2);plot(t,x,t1,x1,'rp');
ylim([-1,1]);
title('采样过程');
%绘出采样后的信号
subplot(3,1,3);plot(t1,x1);
ylim([-1,1]);xlabel('时间/s');
title('采样后信号');
